public class Skyscraper {
    public Skyscraper() {
        System.out.println("The skyscraper has been built.");
    }

    public Skyscraper(int numberOfFloors) {
        System.out.println("The skyscraper with " + numberOfFloors + " floors has been built.");
    }

    public Skyscraper(String developer) {
        System.out.println("The skyscraper developed by " + developer + " has been built.");
    }
}